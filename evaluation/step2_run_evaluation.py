"""
step2_run_evaluation.py
------------------------
Read the raw RAG outputs (generated by step1) and compute six evaluation metrics:

1. Context Precision   – fraction of retrieved chunks relevant to the question
2. Context Recall      – fraction of ground‑truth facts present in the chunks
3. Context Relevancy   – how specifically the chunks address the question
4. Faithfulness        – whether the answer is fully supported by the given context
5. Answer Relevancy    – whether the answer directly addresses the question
6. Exact Match (Numeric) – objective number overlap

Metrics 1–5 are scored by a Groq LLM judge (two separate prompts per question).
The results are written to CSV, Excel, and a summary text file.
"""

import sys
import json
import re
import time
from pathlib import Path

import pandas as pd

THIS_DIR = Path(__file__).resolve().parent
sys.path.insert(0, str(THIS_DIR))

from rag_pipeline import groq_chat
from numeric_match import exact_match_score

RESULTS_DIR = THIS_DIR / "results"
RAW_PATH = RESULTS_DIR / "rag_raw_outputs.json"
SCORED_PATH = RESULTS_DIR / "evaluation_scored.json"
CSV_PATH = RESULTS_DIR / "evaluation_detailed.csv"
XLSX_PATH = RESULTS_DIR / "evaluation_report.xlsx"
SUMMARY_PATH = RESULTS_DIR / "evaluation_summary.txt"

MAX_CHUNKS_FOR_JUDGE = 12
MAX_CHARS_PER_CHUNK = 1500
SLEEP_BETWEEN_QUESTIONS = 1.5

GREEN, YELLOW = 0.80, 0.50


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
def safe_json_parse(raw):
    if not raw:
        return None
    text = raw.strip()
    if text.startswith("```"):
        text = text.strip("`")
        if text.lower().startswith("json"):
            text = text[4:]
        text = text.strip()
    try:
        return json.loads(text)
    except Exception:
        pass
    match = re.search(r"\{.*\}", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(0))
        except Exception:
            pass
    return None


def clamp01(x, default=None):
    try:
        x = float(x)
    except (TypeError, ValueError):
        return default
    return max(0.0, min(1.0, x))


# ---------------------------------------------------------------------------
# Judge prompt A: Context Precision, Recall, Relevancy
# ---------------------------------------------------------------------------
def judge_context_quality(question, ground_truth, contexts):
    judged_chunks = contexts[:MAX_CHUNKS_FOR_JUDGE]
    n = len(judged_chunks)
    if n == 0:
        return {"context_precision": None, "context_recall": 0.0, "context_relevancy": 0.0,
                "chunks_judged": 0, "chunk_relevance_raw": None}

    numbered = "\n\n".join(
        f"[{i + 1}] {c[:MAX_CHARS_PER_CHUNK]}" for i, c in enumerate(judged_chunks)
    )

    prompt = f"""You are an impartial evaluation judge for a Retrieval-Augmented-Generation (RAG) system that answers questions about the Digital Personal Data Protection Act, 2023.

QUESTION: {question}

GROUND TRUTH (correct) ANSWER: {ground_truth}

RETRIEVED CHUNKS (numbered, in the order the retriever returned them):
{numbered}

Evaluate the retrieved chunks against the question and the ground-truth answer:

1. chunk_relevance: for EACH of the {n} chunks above, decide if it is genuinely useful for answering the QUESTION (directly on-topic and would help produce the ground-truth answer). Return exactly {n} integers (1 = relevant, 0 = not relevant), in the same order as the chunks.
2. recall_score: considering ALL the chunks together, what fraction (0.0 to 1.0) of the specific facts stated in the GROUND TRUTH answer are actually present somewhere in the chunks? (e.g. if the ground truth states a penalty amount AND a section number, and only the section number appears in the chunks, recall is around 0.5, not 1.0).
3. relevancy_score: 0.0 to 1.0 - do the chunks address the SPECIFIC intent of the question, not just the general topic? 1.0 = chunks directly and specifically answer what was asked; 0.0 = chunks are only generally on-topic but miss the specific intent.

Return ONLY this JSON object, nothing else, no markdown fences, no commentary:
{{"chunk_relevance": [0 or 1, ...], "recall_score": <float>, "relevancy_score": <float>}}"""

    raw = groq_chat(
        [{"role": "user", "content": prompt}],
        temperature=0.0, max_tokens=1024, json_mode=True,
    )
    data = safe_json_parse(raw)

    context_precision = None
    chunk_relevance_raw = None
    if data and isinstance(data.get("chunk_relevance"), list):
        chunk_relevance_raw = data["chunk_relevance"]
        if len(chunk_relevance_raw) == n:
            try:
                context_precision = sum(int(bool(x)) for x in chunk_relevance_raw) / n
            except Exception:
                context_precision = None

    recall = clamp01(data.get("recall_score")) if data else None
    relevancy = clamp01(data.get("relevancy_score")) if data else None

    return {
        "context_precision": context_precision,
        "context_recall": recall,
        "context_relevancy": relevancy,
        "chunks_judged": n,
        "chunk_relevance_raw": chunk_relevance_raw,
    }


# ---------------------------------------------------------------------------
# Judge prompt B: Faithfulness and Answer Relevancy
# ---------------------------------------------------------------------------
def judge_answer_quality(question, used_context, answer, ground_truth):
    context_for_prompt = used_context[:14000] if used_context else "(no context was retrieved)"

    prompt = f"""You are an impartial evaluation judge for a Retrieval-Augmented-Generation (RAG) system that answers questions about the Digital Personal Data Protection Act, 2023.

QUESTION: {question}

CONTEXT THAT WAS GIVEN TO THE ANSWERING MODEL:
{context_for_prompt}

GENERATED ANSWER: {answer}

GROUND TRUTH (correct) ANSWER (reference only - judge faithfulness against the CONTEXT above, not this): {ground_truth}

Evaluate the GENERATED ANSWER:

1. faithfulness_score: 0.0 to 1.0 - is every factual claim in the GENERATED ANSWER actually supported by the CONTEXT above? 1.0 = fully supported, nothing invented or contradicted. 0.0 = the answer contradicts the context or states facts (numbers, names, section references, penalty amounts) that are not present in the context. Penalize any invented or altered numbers/penalties/sections severely - this is the most important check.
2. faithfulness_issues: one short sentence describing what (if anything) was unsupported or fabricated. Empty string if nothing.
3. answer_relevancy_score: 0.0 to 1.0 - does the GENERATED ANSWER directly and concisely address what the QUESTION actually asked, regardless of whether it happens to be correct? 1.0 = directly on point. 0.0 = off-topic, generic, or non-responsive.

Return ONLY this JSON object, nothing else, no markdown fences, no commentary:
{{"faithfulness_score": <float>, "faithfulness_issues": "<string>", "answer_relevancy_score": <float>}}"""

    raw = groq_chat(
        [{"role": "user", "content": prompt}],
        temperature=0.0, max_tokens=512, json_mode=True,
    )
    data = safe_json_parse(raw)
    if not data:
        return {"faithfulness": None, "faithfulness_issues": "(judge response could not be parsed)",
                "answer_relevancy": None}

    return {
        "faithfulness": clamp01(data.get("faithfulness_score")),
        "faithfulness_issues": data.get("faithfulness_issues", ""),
        "answer_relevancy": clamp01(data.get("answer_relevancy_score")),
    }


# ---------------------------------------------------------------------------
# Resumable scoring loop and output generation
# ---------------------------------------------------------------------------
def load_scored():
    if SCORED_PATH.exists():
        with open(SCORED_PATH, "r", encoding="utf-8") as f:
            return json.load(f)
    return []


def save_scored(rows):
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    with open(SCORED_PATH, "w", encoding="utf-8") as f:
        json.dump(rows, f, ensure_ascii=False, indent=2)


def score_label(value):
    if value is None:
        return "N/A"
    if value >= GREEN:
        return "GREEN"
    if value >= YELLOW:
        return "YELLOW"
    return "RED"


def write_outputs(scored):
    df = pd.DataFrame(scored)
    metric_cols = ["context_precision", "context_recall", "context_relevancy",
                   "faithfulness", "answer_relevancy", "exact_match"]

    df.to_csv(CSV_PATH, index=False, encoding="utf-8")
    print(f"\nWrote {CSV_PATH}")

    # Summary
    lines = []
    lines.append("COMPLY.AI - RAG Evaluation Summary")
    lines.append(f"Questions evaluated: {len(df)}")
    lines.append("")
    lines.append(f"{'Metric':<22}{'Average':>10}{'N/A rows':>10}   Status")
    lines.append("-" * 60)

    averages = {}
    for col in metric_cols:
        non_null = df[col].dropna()
        avg = float(non_null.mean()) if len(non_null) else None
        averages[col] = avg
        na_count = int(df[col].isna().sum())
        avg_str = f"{avg:.2f}" if avg is not None else "N/A"
        lines.append(f"{col:<22}{avg_str:>10}{na_count:>10}   {score_label(avg)}")

    lines.append("")
    lines.append(f"Thresholds: GREEN >= {GREEN:.2f}, YELLOW >= {YELLOW:.2f}, RED below {YELLOW:.2f}")
    lines.append("")
    lines.append("Goal: GREEN (>=0.80) on Faithfulness and Exact Match, as these are")
    lines.append("most critical for a compliance tool (hallucinated facts / wrong numbers).")
    lines.append("")
    lines.append("Diagnostic guidance:")
    lines.append("  High Precision + Low Recall    -> retrieval misses coverage. Try increasing")
    lines.append("                                     k or adjusting chunk size/overlap.")
    lines.append("  High Recall + Low Faithfulness -> the right facts ARE retrieved, but the LLM")
    lines.append("                                     still drifts from them. Tighten the system")
    lines.append("                                     prompt to force 'use ONLY the context'.")
    lines.append("  Low Answer Relevancy           -> the LLM is going off-topic. Make the")
    lines.append("                                     instruction more directive.")
    lines.append("  Low Exact Match                -> numbers are being mis-copied or mis-")
    lines.append("                                     attributed to the wrong section/item.")

    with open(SUMMARY_PATH, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    print(f"Wrote {SUMMARY_PATH}")
    print("\n" + "\n".join(lines) + "\n")

    # Excel report
    try:
        with pd.ExcelWriter(XLSX_PATH, engine="openpyxl") as writer:
            df.to_excel(writer, sheet_name="Per-Question", index=False)
            summary_df = pd.DataFrame({
                "metric": metric_cols,
                "average": [averages[c] for c in metric_cols],
                "na_rows": [int(df[c].isna().sum()) for c in metric_cols],
                "status": [score_label(averages[c]) for c in metric_cols],
            })
            summary_df.to_excel(writer, sheet_name="Summary", index=False)
        print(f"Wrote {XLSX_PATH}")
    except Exception as e:
        print(f"   (could not write Excel report: {e})")


def main():
    if not RAW_PATH.exists():
        print(f"ERROR: Raw outputs not found at {RAW_PATH}")
        print("Run step1_generate_responses.py first.")
        sys.exit(1)

    with open(RAW_PATH, "r", encoding="utf-8") as f:
        raw_items = json.load(f)
    print(f"Loaded {len(raw_items)} RAG outputs from {RAW_PATH.name}")

    scored = load_scored()
    done_questions = {r["question"] for r in scored}
    if done_questions:
        print(f"Resuming: {len(done_questions)} questions already scored.\n")

    total = len(raw_items)
    for i, item in enumerate(raw_items, 1):
        question = item["question"]
        if question in done_questions:
            print(f"[{i}/{total}] Skipping (already scored): {question[:70]}")
            continue

        print(f"[{i}/{total}] Scoring: {question[:80]}")
        ground_truth = item["ground_truth"]
        contexts = item.get("contexts", [])
        used_context = item.get("used_context", "")
        answer = item.get("answer", "")

        try:
            ctx_result = judge_context_quality(question, ground_truth, contexts)
        except Exception as e:
            print(f"   !! context judge failed: {e}")
            ctx_result = {"context_precision": None, "context_recall": None,
                          "context_relevancy": None, "chunks_judged": len(contexts[:MAX_CHUNKS_FOR_JUDGE])}

        try:
            ans_result = judge_answer_quality(question, used_context, answer, ground_truth)
        except Exception as e:
            print(f"   !! answer judge failed: {e}")
            ans_result = {"faithfulness": None, "faithfulness_issues": str(e), "answer_relevancy": None}

        exact = exact_match_score(ground_truth, answer)

        scored.append({
            "question": question,
            "ground_truth": ground_truth,
            "answer": answer,
            "num_chunks_retrieved": len(contexts),
            "num_chunks_judged": ctx_result.get("chunks_judged", 0),
            "context_precision": ctx_result.get("context_precision"),
            "context_recall": ctx_result.get("context_recall"),
            "context_relevancy": ctx_result.get("context_relevancy"),
            "faithfulness": ans_result.get("faithfulness"),
            "faithfulness_issues": ans_result.get("faithfulness_issues", ""),
            "answer_relevancy": ans_result.get("answer_relevancy"),
            "exact_match": exact,
        })
        save_scored(scored)
        time.sleep(SLEEP_BETWEEN_QUESTIONS)

    write_outputs(scored)


if __name__ == "__main__":
    main()